export type SupportedLanguage = keyof typeof suggestedQuestions;

// Helper function to get a random question
export function getRandomQuestion(lang: SupportedLanguage = "en"): string {
  const questions = suggestedQuestions[lang] || suggestedQuestions.en;
  const randomIndex = Math.floor(Math.random() * questions.length);
  return questions[randomIndex];
}
